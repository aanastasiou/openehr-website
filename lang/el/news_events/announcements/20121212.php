			<p>At present the Board is:</p>
			<ul>
				<li>Requesting all Program Leads to develop a register of Qualified Members who can vote for Board Members</li>
				<li>Holding a meeting of potential Industry Partners to develop a strategy of engagement and the rights and responsibilities of these organisational members</li>
				<li>Holding a meeting of potential Academic Partners to do likewise</li>
				<li>Seeking to develop the arrangements by which countries might become National Members of the Foundation and have access to a national Clinical Knowledge Manager instance to develop archetypes, templates and term sets for use in their own jurisdiction as well as keep control of which international assets are used.</li>
			</ul>
			<p>The Board will be meeting again soon to assess progress in these areas.</p>
