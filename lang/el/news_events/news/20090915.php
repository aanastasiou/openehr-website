			<h1>News</h1>
			
			<h2>IHTSDO and openEHR Begin Collaborative Work Programme</h2>
			<h6>15. September 2009 | from: David Ingram</h6>
			
			<br/>
			<p>IHTSDO and the openEHR Foundation are to work together on a harmonisation project where the overall goal is the practical development of effective and sustainable clinical content for the electronic health record. The project will explore how best to support those who wish to use openEHR archetypes and SNOMED CT terminology together within current and future systems to support data capture, complex queries, clinical decision support and reporting.</p>
			<p>This initiative arose from an intergovernmental workshop with high-level industry representation held in Helsingor, Denmark in November 2008, where the openEHR Foundation and the IHTSDO were invited to present their perspectives on how health informatics standards could best interact and contribute to meeting the common needs of large-scale health information infrastructure initiatives, worldwide. In response to this call for leadership and wider consultation, IHTSDO and openEHR have resolved to identify opportunities to align efforts to address the practical implementation and evaluation challenges facing national eHealth programs, in a coordinated way.</p>
			<p>This work will be of immediate interest and relevance in countries where the use of clinical data archetypes and clinical terminology are already envisaged as part of the standardisation process. Further, a growing number of e-health system vendors are recognising that standardisation of content is difficult to achieve within their own applications without agreement on the structure of the information. In the first instance, IHTSDO and openEHR recognize the potential value to these stakeholders and others in more active harmonization efforts, including establishing principles to guide collaborative activities.</p>
			<p>It is planned that a first practical focus of the joint effort will be mutual engagement with and support of a member-led project to develop a logical record architecture through the UK Terminology Centre (the IHTSDO National Release Centre for the UK). The intent is then to take this work forward within a wider context, including goals for pan-European semantic interoperability of health records, the Framework Programme and the EuroRec Institute. The hope is that this plan for collaboration will provide initial stepping stones towards global extension of the work. To that end, it is envisaged that strategic oversight for the joint programme will come from the wider global communities of the two organisations.</p>
			<p>IHTSDO and openEHR will draw from their respective pools of skilled and experienced members to create the strongest possible team to take forward the programme of work. They will also continue to collaborate, separately and together, with others who wish to align with and work with them towards these shared goals. They are resolved to use the experience gained as the basis for confirming the collaborative vision, as well as aligning the products of the two organisations within sustainable international governance and with clinical, professional, ehealth program and industry engagement.</p>
			
			
			<h2>About the International Health Terminology Standards Development Organisation (IHTSDO)</h2>
			<p>IHTSDO and its Members seek to improve the health of humankind by fostering the development and use of suitable standardized clinical terminologies, notably SNOMED Clinical Terms (SNOMED CT), in order to support the safe, accurage, and effective exchange of health information. SNOMED CT is a standardized terminology that can be used as the foundation for electronic health records and other applications. It contains more than 310,000 unique concepts and more than 1.3 million links or relationships between them that ensure that this information is captured consistently, accurately, and reliably across the health system. SNOMED CT was originally created by the College of American Pathologists by combining SNOMED RT and a computer-based nomenclature and classification known as Clinical Terms Version 3, formerly known as Read Codes Version 3, which was created on behalf of the UK Department of Health and is Crown copyright. More information about IHTSDO is available at <a href="http://www.ihtsdo.org">IHTSDO website</a>.</p>
			
			
			<h2>About the openEHR Foundation</h2>
			<p>openEHR is a long standing contributor to evolving concepts and standards for the structural architecture of the electronic health record, based on rigorous specification of clinical data archetypes. It has focussed its efforts first on well formulated clinical requirements and then on establishing a now worldwide community of interest in rigorous open specifications for the electronic health record, tested through wide-scale implementation, practical clinical engagement and a spirit of collaborative and community-driven work. It is, as yet, a voluntary organisation with no paid employees, but is established as a not-for-profit company at UCL in London and is the centre of a growing informal international network of members and companies, drawing together a considerable body of published, implemented and freely available work, which underpins the new ISO 13606 standard for the clinical data archetype. More information about openEHR is available in the section <a href="../../about/foundation">About Us</a>.</p>
